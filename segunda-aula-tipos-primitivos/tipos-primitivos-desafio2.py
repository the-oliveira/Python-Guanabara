x = input('Digite uma palavra: ')


print('O tipo primitivo do valor é: {}'.format(type(x)))
print('É alfabético? {}'.format(x.isalpha()))
print('Só tem espaço? {}'.format(x.isspace()))
print('É um número? {}'.format(x.isnumeric()))
print('É um decimal? {}'.format(x.isdecimal()))
print('É um alfanumérico? {}'.format(x.isalnum()))
print('Está em letras maiúsculas? {}'.format(x.isupper()))
print('Está em letras minúsculas? {}'.format(x.islower()))






